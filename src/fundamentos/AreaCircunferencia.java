package fundamentos;

public class AreaCircunferencia {

	public static void main(String[] args) {

		int raio = 10; // variavel de numero inteiro
		 final double PI = 3.14; // final transforma em constante, deixar a variavel em letra maiuscula 
		double area = PI * raio * raio; // variavel com casa decimal

		System.out.println(2 + 3);
		System.out.println( "area = " +area); // o "+" junta texto com variaveis concatenando 
		

	}

}
